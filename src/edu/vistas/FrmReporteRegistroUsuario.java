    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
         Connection Conexion=null;
         JasperReport reporte;
          String fecha1=jformatInicio.getText();
          String fecha2=jformatfin.getText();
          Map parameters = new HashMap();
          parameters.put("fecha1",fecha1);
          parameters.put("fecha2",fecha2);
    
        try 
        {
            Class.forName("com.mysql.jdbc.Driver");
            Conexion=DriverManager.getConnection("jdbc:mysql://localhost:3306/proyectosse","root","");
        } 
        catch (ClassNotFoundException | SQLException e) 
        {
            System.out.println(e.getMessage());
        }
        try {
            reporte=JasperCompileManager.compileReport("src/edu/reportes/reporteRegistroUser.jrxml");
            JasperPrint jp= JasperFillManager.fillReport(reporte,parameters,Conexion);
            JasperViewer.viewReport(jp,false);
        } catch (JRException ex) {
            Logger.getLogger(FrmReporteRegistroUsuario.class.getName()).log(Level.SEVERE,null,ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

